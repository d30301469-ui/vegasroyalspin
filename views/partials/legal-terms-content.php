    </ul>
    <h5>2. Hesap Kaydı</h5>
    <ul>
        <li>Hizmetlerimizi kullanmak için doğru ve eksiksiz bilgi sağlayarak bir hesap oluşturmalısınız.</li>
        <li>Hesap bilgilerinizin gizliliğini korumaktan ve hesabınız altında gerçekleşen tüm faaliyetlerden siz sorumlusunuz.</li>
    </ul>
